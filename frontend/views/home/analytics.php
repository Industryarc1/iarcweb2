         <strong>Customer Analytics</strong>
        <p>  The usage of data from customer behavior with respect to an organization to make strategic business decisions using predictive analytics is called customer analytics. Customer analytics can be used by companies for customer relationship management, targeted marketing, maximize customer retention, accurate sales forecasting and so on.
        </p>
         <strong>Marketing Analytics</strong>
        <p>   Companies invest significant amount of time, resources and money into their marketing campaigns and measuring the performance of these initiatives can be called as marketing analytics. Data from various channels like blogs, social media sites, website etc are gathered and analyzed to help understand the return on investment, marketing efficiency, target reach and identification of channel deficiencies etc.
        </p>
         <strong>Operational Analytics</strong>
        <p> Every industry has different processes and operations in place to ensure that the service or product reaches the end client on time. Operational Analytics helps a company in studying these processes in-depth with assistance from KPIs, dashboards and reporting tools. Real time analysis of this kind will help companies to increase transactions, reduce risk and fraud, optimize production and distribution channels, efficient human and capital resource management and streamline operational processes according to consumer demand changes.
        </p>
         <strong>Business Intelligence</strong>
        <p>  Business Intelligence or BI is the transformation of large unstructured data sets or raw data into meaningful, structured data which can be converted into actionable metrics or strategies for a company. Big data is a key tool which is expected to play a major role across industries in providing business intelligence by utilizing raw data gathered through data mining, text, process mining etc and give a historic or real time view of data flow.
        </p>
         <strong>Competitor Analysis</strong>
        <p> The assessment of strengths, weaknesses of the competitors in the context of the overall opportunities and threats in the operating market of a company can be termed as competitor analysis. Product benchmarking, financial analysis, SWOT analysis, plant capacity utilization, corporate and marketing strategies, patents, R&D budgets are the key factors upon which competitor analysis can be done.
        </p>
         <strong>Optimal Resource Allocation</strong>
        <p> Capital, infrastructure and employees are the key resources for any company and optimal utilization of these resources will increase longevity, efficiency, throughput and decrease costs for the organization. By studying the business processes, budgets and other key factors in the business model of a company, optimal resource allocation can be done.
        </p>
         <strong>Recommendations and Promotions</strong>
        <p> Retail companies and E-commerce companies have a wealth of data accumulating from their Point of Sale (PoS) terminals, historic purchase patterns, digital shopping trends, social media chatter etc. This data can be mined to understand and predict customer needs, shopping patterns and also make optimal utilization of warehouse and inventory. Recommendations with coupons, discounts to customers and promotion campaigns with offers can drive sales from competitors to the company. This is especially crucial to increase sales, maintain profitability during holidays or seasonal low demand or when there is increased competition in the market.
        </p>
